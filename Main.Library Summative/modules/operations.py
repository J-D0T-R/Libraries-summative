# Imports
import math

# Functions
def add(x,y):
    print(x + y)
    return(x + y)

def subtract(x,y):
    print(x - y)
    return(x - y)

def multiply(x,y):
    print(x * y)
    return(x * y)

def divide(x,y):
    print(x / y)
    return(x / y)

def power(x,y):
    print(math.pow(x,y))
    return(math.pow(x,y))

def root(x,y):
    print(x ** (1.0/y))
    return(x ** (1.0/y))


